"""Keyword glossary shared across combat and non-combat prompts.

Scans card rules_text for known keywords and returns concise definitions.

Priority chain for mechanic lookups:
  1. KW_GLOSSARY — hand-written, authoritative definitions (preferred)
  2. _dll_mechanics — auto-extracted from game DLL at agent startup (fallback for
     mechanics not yet in KW_GLOSSARY; never falls back to local markdown/json KB)
"""

